• Top 3 Customers with the Highest Total Balance Across All Accounts
SELECT TOP 3 
    c.CustomerID,
    c.FullName,
    SUM(a.Balance) AS TotalBalance
FROM Customers c
JOIN Accounts a ON c.CustomerID = a.CustomerID
GROUP BY c.CustomerID, c.FullName
ORDER BY TotalBalance DESC;

• Customers Who Have More Than One Active Loan
SELECT 
    c.CustomerID,
    c.FullName,
    COUNT(l.LoanID) AS ActiveLoanCount
FROM Customers c
JOIN Loans l ON c.CustomerID = l.CustomerID
WHERE l.Status = 'Active'
GROUP BY c.CustomerID, c.FullName
HAVING COUNT(l.LoanID) > 1;

• Transactions That Were Flagged as Fraudulent 
SELECT 
    t.TransactionID,
    t.AccountID,
    t.Amount,
    t.Date,
    f.RiskLevel,
    f.ReportedDate
FROM FraudDetection f
JOIN Transactions t ON f.TransactionID = t.TransactionID
WHERE f.RiskLevel IN ('High', 'Critical');

• Total Loan Amount Issued Per Branch
SELECT 
    b.BranchID,
    b.BranchName,
    SUM(l.Amount) AS TotalLoanAmount
FROM Loans l
JOIN Customers c ON l.CustomerID = c.CustomerID
JOIN Accounts a ON c.CustomerID = a.CustomerID
JOIN Branches b ON a.BranchID = b.BranchID
GROUP BY b.BranchID, b.BranchName
ORDER BY TotalLoanAmount DESC;

